
class User {
    protected string Name;
    protected int Count;
    protected int ID;
    protected int ManegerID;


    public User( 
      string Name, 
      int Count, 
      int ID, 
      int ManegerID) 
      {
        this.Name = GetName = Name;
        this.Count = GetCount = Count;
        this.ID = GetID = ID;
        this.ManegerID = GetManegerID = ManegerID;



    }

   public string GetName {get;}
   public int GetCount {get;}
   public int GetID {get;}
   public int GetManegerID {get;}



  }